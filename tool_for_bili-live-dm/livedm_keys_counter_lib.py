def _analyze_structure(cmd: str, depth: int, item: int | str | list | dict | bool | None, target_key=""):
    # init cmd
    if depth == 1:
        if cmd not in result:
            result[cmd] = {}
    # init cmd.key[.key]
    final_key = f"{cmd}{target_key}"
    if final_key not in (f"{cmd}.cmd", f"{cmd}.msg_id", f"{cmd}.send_time", cmd):
        if result[cmd].get(final_key) is None:
            result[cmd][final_key] = {}
        if result[cmd][final_key].get("type") is None:
            result[cmd][final_key]["type"] = []
        if type(item).__name__ not in result[cmd][final_key]["type"]:
            result[cmd][final_key]["type"].append(type(item).__name__)
    if isinstance(item, dict):
        for key, value in item.items():
            _analyze_structure(cmd, depth + 1, value, f"{target_key}.{key}")
    elif isinstance(item, list):
        for index, li in enumerate(item):
            _tk = f"{target_key}[IDX]" if final_key in _DONT_CARE_INDEX_LIST else f"{target_key}[{index}]"
            _analyze_structure(cmd, depth + 1, li, _tk)
    if final_key in (f"{cmd}.cmd", f"{cmd}.msg_id", f"{cmd}.send_time", cmd):
        return
    if isinstance(item, (dict, list)):
        return
    if result[cmd][final_key].get("value") is None:
        result[cmd][final_key]["value"] = {}
    if final_key in _IGNORE_LIST:
        return
    t1 = repr(item)
    if result[cmd][final_key]["value"].get(t1) is None:
        result[cmd][final_key]["value"][t1] = 0
    result[cmd][final_key]["value"][t1] = result[cmd][final_key]["value"][t1] + 1


def main(in_paths: list[str], output_dir: str):
    if not in_paths:
        return
    for in_path in in_paths:
        lineno = 1
        if not os.path.exists(output_dir):
            os.makedirs(output_dir)
        if in_path == output_dir:
            continue
        with open(in_path, "r", encoding="utf-8") as file_in:
            for line in file_in.readlines():
                lineno += 1
                try:
                    item: dict = json.loads(line[line.find("{") :])
                except json.JSONDecodeError:
                    continue
                _analyze_structure(cmd=item["cmd"], depth=1, item=item)
    return result
